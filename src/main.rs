use anyhow::Result;

mod cli;
mod session;

fn main() -> Result<()> {
    tracing_subscriber::fmt()
        .with_env_filter(
            tracing_subscriber::EnvFilter::try_from_default_env()
                .unwrap_or_else(|_| tracing_subscriber::EnvFilter::new("warn")),
        )
        .init();

    recall::db::schema::register_sqlite_vec();

    cli::run()
}
